import UIKit.UIView

public extension UIView {
    var midHeight: CGFloat { bounds.size.height * 0.50 }
    var doubleHeight: CGFloat { bounds.size.height * 2.0 }
    var oneThird: CGFloat { bounds.size.height * (1/3) }
    var twoThirds: CGFloat { bounds.size.height * (2/3) }
}
